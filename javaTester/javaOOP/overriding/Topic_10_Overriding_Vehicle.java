package javaOOP.overriding;

public class Topic_10_Overriding_Vehicle {
	
	protected void engine() {
		System.out.println("A vehicle need to an engine to run");
	}
	
	protected void brake() {
		System.out.println("A vehicle need to a brake to stop.");
	}
	
	protected void fuel() {
		System.out.println("A vehicle need to fuel to starting engine");
	}
}
